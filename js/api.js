const API_KEY = "tsShv4yJ"

async function fetchController(controller, lat = null, lng = null) {
  let method = "getall";
  let params = "&limit=9999";
  
  if (lat !== null && lng !== null) {
    method = "getfromlatlng";
    params = `&lat=${lat}&lng=${lng}&radius=1000&limit=9999`;
  }

  const url = `https://smapi.lnu.se/api/?api_key=${API_KEY}&controller=${controller}&method=${method}${params}`;
  try {
    const response = await fetch(url);
    const data = await response.json();
    const payload = data.payload || [];
    return payload.map((item) => ({ ...item, originController: controller }));
  } catch (error) {
    console.error(`Misslyckades att hämta från ${controller}:`, error);
    return [];
  }
}

export async function fetchAllData(userLat = null, userLng = null) {
  const establishments = await fetchController("establishment", userLat, userLng);
  const validCategories = ["museum", "slott", "kyrka"];
  
  const filteredData = establishments.filter((place) => {
    if (place.id === "660" || place.id === "517" || place.id === "163") return false;
    
    const category = place.description ? place.description.toLowerCase().trim() : "";
    
    if (validCategories.includes(category)) {
      place.customCategory = category; 
      place.distance = place.distance_in_km ? parseFloat(place.distance_in_km) : 999;
      return true;
    }
    return false;
  });
  
  return filteredData;
}

export async function fetchSingle(id) {
  const urlEstablishment = `https://smapi.lnu.se/api/?api_key=${API_KEY}&controller=establishment&method=getall&ids=${id}`;

  try {
    let response = await fetch(urlEstablishment);
    let data = await response.json();
    if (data.payload && data.payload.length > 0) return data.payload[0];
    return null;
  } catch (error) {
    return null;
  }
}

